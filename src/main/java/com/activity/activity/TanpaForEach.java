package com.activity.activity;

public class TanpaForEach {
    public static void main(String[] args) {
        String[] array = {
            "Affan", "Nabil", "Ramadhan",
            "Programmer", "Pintar", "Joss"
        };
        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }
    }
}